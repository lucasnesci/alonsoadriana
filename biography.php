    <h3>Introduccion</h3>
    Nacida en la ciudad de Rosario, Pcia. de Santa Fe.<br/>
    Graduada en Cs. Económicas en la ciudad de Rosario el 20 de febrero de 1985.<br/>
    Graduada en Decoración de Interiores. Año 1987.<br/>
    Desde 1985 hasta 2001 cursa estudios de pintura decorativa y acrílica en distintos talleres particulares.<br/>
    Durante el año 2009 cursado estudios de pintura impresionista con el Maestro Ambrosio Gatti. <br/>
    Desde 2003 al 2011 estudios de pintura impresionista con la Prof. Nelly Cortizo<br/>
    Desde 2004 a la actualidad estudios de dibujo y pintura con el Prof. Miguel Ballesteros.<br/>
    Desde el 2011 a la actualidad cursando estudios con la artista Cecilia Garate.<br/>
    2012 Curso de dibujo con Prof Aldo Chacal.<br/>
    2013 Curso de pintura con espátula `con Profesor José Cordi.<br/>
    2012 2013 Seminario pintura espátula prof. Darío Mastrocimone.<br/>
    
